👨‍🍳 作り方
① 牛肉を炒める

フライパンにごま油を熱し、牛こま切れ肉を中火で炒めます。肉の色が変わったら焼肉のたれを加え、全体に味がなじむまでさらに2〜3分炒めます。

② もやしのナムルを作る

もやしを2〜3分茹で、水気をしっかり切ります。ボウルに入れ、ごま油・鶏ガラスープの素・塩を加えてよく和えます。

③ ほうれん草のナムルを作る

ほうれん草をさっと茹でて冷水に取り、水気をよく絞ります。3〜4cm幅に切り、ごま油・醤油・白ごまで味付けします。

④ にんじんを炒める

にんじんを細切りにし、ごま油を熱したフライパンで軽く炒めます。塩を加えてさらに1〜2分炒めます。

⑤ 盛り付ける

どんぶりに温かいご飯を盛り付けます。牛肉・もやし・ほうれん草・にんじん・キムチを彩りよく並べ、中央に温泉卵をのせます。

⑥ 仕上げ

白ごまを全体に振り、お好みでコチュジャンを添えます。食べる前によく混ぜると、それぞれの具材の味が合わさってよりおいしくいただけます。

💡 ワンポイント
牛肉は豚こま肉でも代用できます。
コチュジャンを多めに入れると、より本格的な韓国風の味わいになります。
フライパンで軽く焼いて「石焼き風」にすると、おこげの香ばしさも楽しめます。
